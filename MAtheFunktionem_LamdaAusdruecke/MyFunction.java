/**
 * Das Interface MyFunction definiert eine Funktion, 
 * die einen ganzzahligen Eingabewert akzeptiert.
 *
 * @author Grace D. Ntiwa Kanou
 * @author Sandra R. Hussong
 * 
 * @version 22.05.23
 */
public interface MyFunction
{
     /**
     * Wendet die Funktion auf den gegebenen Eingabewert 
     * an und gibt den entsprechenden Ausgabewert zurueck.
     * 
     * @param i Der Eingabewert, auf den die Funktion 
     *          angewendet wird
     */
    public int apply(int i);
}